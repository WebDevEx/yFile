  onPrepare: async function (config, capabilities) {
    // start an express server that hosts the demo, injects window.INTEGRATION_TEST, and resolves "yfiles" imports
    const app = express()
    const staticRoot = path.join(__dirname, '../../..')

    // inject a script that sets the INTEGRATION_TEST variable, so
    // the app knows it should expose the yFiles API globally for testing
    app.use((req, res, next) => {
      const ext = path.extname(req.path)
      if (ext !== '.html') {
        next()
        return
      }
      const filePath = path.join(staticRoot, req.path)
      fs.readFile(filePath, 'utf8', (err, data) => {
        if (err) {
          next()
          return
        }
        data = data.replace('<head>', '<head><script>window.INTEGRATION_TEST=true</script>')
        res.type(ext)
        res.send(data)
      })
    })

    // resolves "yfiles" imports in .js files
    app.use(resolveYfiles({ staticRoot }))
    app.use(
      '/',
      express.static(staticRoot, {
        index: ['README.html', 'index.html', 'index.htm']
      })
    )

    demoServer = app.listen(demoServerPort, () => {
      console.log(`Testing Demo Server listening at localhost:${demoServerPort}`)
    })
  },
